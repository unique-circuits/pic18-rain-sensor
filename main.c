#include <pic18f4550.h>


 
#define RAIN PORTDbits.RD0                  //Rain sensor Output is connected at PORTD.0
 
#define rs PORTCbits.RC0
#define rw PORTCbits.RC1
#define en PORTCbits.RC2
#define delay for(i=0;i<1000;i++)
 
int i;
 
void lcd_init();
void cmd(unsigned char a);
void dat(unsigned char b);
void show(unsigned char *s);
 
 
void main()
{
    /* PORT direction configuration */
    TRISB = 0x0;
    TRISC = 0x0;
    TRISD = 0x01;    
            
            
    lcd_init();
    show("Unique Circuits");
    cmd(0xc0);
    while(1) {
        if(RAIN == 0) {
            cmd(0xc0);
            show("Rain Detected");
            delay;
        } else {
            cmd(0xc0);
            show("                ");
            delay;
        }
    }
}
 
void lcd_init()
{
    cmd(0x38);
    cmd(0x0c);
    cmd(0x06);
    cmd(0x80);
}
 
void cmd(unsigned char a)
{
    PORTB=a;
    rs=0;
    rw=0;
    en=1;
    delay;
    en=0;
}
 
void dat(unsigned char b)
{
    PORTB=b;
    rs=1;
    rw=0;
    en=1;
    delay;
    en=0;
}
 /* Write string to LCD */
void show(unsigned char *s)
{
    while(*s)
    {
        dat(*s++);
    }
}
